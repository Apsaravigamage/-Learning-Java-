public class Switch{
    public static void main(String a[])
    {
        int i=0;

        switch(i)
        {
            case 0:
                 System.out.println("0");
                 break;
            case 1:
                 System.out.println("1");
                 break;
            case 5:
                 System.out.println("5");
                 break;
            default:
                 System.out.println("some number");

        }
       
    }
}