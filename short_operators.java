public class short_operators{
    public static void main(String a[])
    {
       //short hand operators +=, -=, *= ...
       int i=5;

       i += 2; //i = i + 2
       System.out.println(i);

        i -= 2; //i = i - 2
       System.out.println(i);

        i *= 2; //i = i * 2
       System.out.println(i);

        i /= 2; //i = i / 2
       System.out.println(i);
    }
}