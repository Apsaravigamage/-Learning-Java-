public class ForLoop{
    public static void main(String a[])
    {
        //While
        int i =1;
        while(i<=10)
        {
            System.out.println(i);
            i++;
        }

        // Do While
        int x=1;
        do
        {
            System.out.println(x);
            x++;
        }while(x<10);

        //For
        for(int n=1;n<=10;n++)
        {
            System.out.println(n);
        }


    }
}