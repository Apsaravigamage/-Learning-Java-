class addVariable{
    public static void main(String a[])
    {
        int num1 = 6;
        int num2 = 6;
        System.out.println(num1+num2);

        float n1 = 12.6f;//4 bytes 
        float n2 = 2.2f;
        System.out.println(n1+n2);

        char c ='A';
        System.out.println((int)c); //ASCII value of character
    }
}